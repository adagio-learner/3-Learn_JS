// print value of pi
console.log(Math.PI);

// print maximum
console.log(Math.max(60, 30, 24, 200, 712));

// print minimum
console.log(Math.min(60, 30, 24, 200, 712));

// print round off value
console.log(Math.round(1.4));
console.log(Math.round(1.5));

// print previous closest round off
console.log(Math.floor(1.4));
console.log(Math.floor(1.9));

// print next closest round off
console.log(Math.ceil(1.1));
console.log(Math.ceil(1.9));

// print positive value
console.log(Math.abs(45));
console.log(Math.abs(-875));

// print random number in the range of 0-1
console.log(Math.random())

// print square root of the number
console.log(Math.sqrt(25))

// print the multiplication of first number till second number times
console.log(Math.pow(2, 10))
